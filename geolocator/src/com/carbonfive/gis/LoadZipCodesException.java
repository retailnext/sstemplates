package com.carbonfive.gis;

public class LoadZipCodesException
  extends RuntimeException
{
  public LoadZipCodesException() { super(); }
  public LoadZipCodesException(String msg) { super(msg); }
  public LoadZipCodesException(Throwable cause) { super(cause); }
  public LoadZipCodesException(String msg, Throwable cause) { super(msg, cause); }
}